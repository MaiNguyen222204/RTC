uint8_t decToBcd(int val)
{
  return (uint8_t)( (val/10*16) + (val%10) ); // doi tu thap phan sang nhi phan
}
int bcdToDec(uint8_t val) 
{
  return (int)( (val/16*10) + (val%16) ); // doi tu nhi phan sang thap phan
}
void Set_Time (uint8_t sec, uint8_t min, uint8_t hour, uint8_t dow, uint8_t dom, uint8_t month, uint8_t year)
{
	uint8_t set_time[7];
	set_time[0] = decToBcd(sec);
	set_time[1] = decToBcd(min);
	set_time[2] = decToBcd(hour);
	set_time[3] = decToBcd(dow);     // Th? (0-6)
	set_time[4] = decToBcd(dom);     // Ngày
	set_time[5] = decToBcd(month);   // Tháng
	set_time[6] = decToBcd(year);    // Nam

	HAL_I2C_Mem_Write(&hi2c2, DS1307_ADDRESS, 0x00, 1, set_time, 7, 1000);
}
void Get_Time (void)
{
	uint8_t get_time[7];
	HAL_I2C_Mem_Read(&hi2c2, DS1307_ADDRESS, 0x00, 1, get_time, 7, 1000);
	giay= bcdToDec(get_time[0]);
	phut = bcdToDec(get_time[1]);
	gio = bcdToDec(get_time[2]);
	thu = bcdToDec(get_time[3]);
	ngay = bcdToDec(get_time[4]);
	thang = bcdToDec(get_time[5]);
	nam = bcdToDec(get_time[6]);
	
}
